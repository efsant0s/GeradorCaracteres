    private void gerarCodigo() {
        char[] caracteres = inputText.getText().toCharArray();
        for (int i = 0; i < caracteres.length; i++) {
            if((jCheckBox1.isSelected()) && (i % 25 == 0 && i!=0)){
                textArea.setText(textArea.getText() + "\n");
            }
            if(i + 1 ==  caracteres.length){                 
               textArea.setText(textArea.getText() + " " + GeradorCaracteres.geraCaractere(caracteres[i]) + "."); 
            }else{                 
                textArea.setText(textArea.getText() + " " + GeradorCaracteres.geraCaractere(caracteres[i]) + ", ");
            }
            
        }
    }
